package com.xassure.suiteManagement;

public class TestCaseDetails {

	String testcaseName;
	String testcaseType;
	String testcaseBrowser;
	String moduleName;

	public String getTestcaseName() {
		return testcaseName;
	}

	public void setTestcaseName(String testcaseName) {
		this.testcaseName = testcaseName;
	}

	public String getTestcaseType() {
		return testcaseType;
	}

	public void setTestcaseType(String testcaseType) {
		this.testcaseType = testcaseType;
	}

	public String getTestcaseBrowser() {
		return testcaseBrowser;
	}

	public void setTestcaseBrowser(String testcaseBrowser) {
		this.testcaseBrowser = testcaseBrowser;
	}

	public String getModuleName() {
		return moduleName;
	}

	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}

}
